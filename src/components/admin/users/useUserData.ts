import { useState, useEffect } from 'react';
import { User } from "../types";
import { supabase } from "@/integrations/supabase/client";

export const useUserData = (isAuthenticated: boolean = true, initialUsers: User[] = []) => {
  const [users, setUsers] = useState<User[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<Error | null>(null);
  const [searchTerm, setSearchTerm] = useState("");
  
  // Filter users based on search term
  const filteredUsers = users.filter(user => 
    user.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
    user.email.toLowerCase().includes(searchTerm.toLowerCase()) ||
    user.role.toLowerCase().includes(searchTerm.toLowerCase())
  );
  
  const fetchUsersFromDatabase = async () => {
    try {
      console.log("üîç === BUSCANDO USU√ÅRIOS DO BANCO DE DADOS (VERS√ÉO CORRIGIDA) ===");
      setLoading(true);
      setError(null);
      
      // Verificar se o usu√°rio tem permiss√£o
      const { data: { session } } = await supabase.auth.getSession();
      console.log("üîê Sess√£o atual:", { 
        hasSession: !!session, 
        userId: session?.user?.id,
        email: session?.user?.email 
      });
      
      if (!session) {
        console.log("‚ùå Usu√°rio n√£o autenticado");
        setUsers([]);
        setLoading(false);
        return;
      }
      
      // Buscar perfis primeiro
      console.log("üë• Buscando todos os perfis...");
      const { data: profiles, error: profilesError } = await supabase
        .from('profiles')
        .select('*');
        
      if (profilesError) {
        console.error("‚ùå Erro ao buscar perfis:", profilesError);
        throw profilesError;
      }
      
      console.log("‚úÖ Perfis encontrados:", profiles?.length || 0);
      console.log("üìã Dados dos perfis:", profiles);
      
      if (!profiles || profiles.length === 0) {
        console.log("‚ö†Ô∏è Nenhum perfil encontrado no banco");
        setUsers([]);
        setLoading(false);
        return;
      }
      
      // Buscar roles separadamente
      console.log("üé≠ Buscando roles...");
      const { data: roles, error: rolesError } = await supabase
        .from('user_roles')
        .select('*');
        
      if (rolesError) {
        console.error("‚ùå Erro ao buscar roles:", rolesError);
        throw rolesError;
      }
      
      console.log("‚úÖ Roles encontradas:", roles?.length || 0);
      console.log("üìã Dados das roles:", roles);
      
      // Combinar perfis com roles
      const usersWithRoles: User[] = profiles.map((profile, index) => {
        const userRole = roles?.find(r => r.user_id === profile.id);
        const role = userRole?.role || 'user';
        
        console.log(`üë§ Processando usu√°rio ${index + 1}:`, {
          email: profile.email,
          role: role,
          firstName: profile.first_name,
          lastName: profile.last_name,
          userId: profile.id
        });
        
        // Mapear roles do banco para roles do frontend
        const roleMapping: Record<string, User["role"]> = {
          'super_admin': 'super_admin',
          'admin': 'admin',
          'instructor': 'instructor',
          'student': 'student',
          'user': 'viewer'
        };
        
        const mappedRole = roleMapping[role] || 'viewer';
        
        const firstName = profile.first_name || 'Usu√°rio';
        const lastName = profile.last_name || '';
        
        return {
          id: index + 1, // ID sequencial para a interface
          name: `${firstName} ${lastName}`.trim(),
          email: profile.email || '',
          role: mappedRole,
          status: 'active' as const,
          createdAt: new Date(profile.created_at),
          lastLogin: new Date() // Placeholder j√° que n√£o temos essa info
        };
      });
      
      console.log("‚úÖ === USU√ÅRIOS PROCESSADOS FINAL ===");
      console.log("üìä Total de usu√°rios:", usersWithRoles.length);
      console.log("üë• Lista completa:", usersWithRoles.map(u => ({ 
        name: u.name, 
        email: u.email, 
        role: u.role 
      })));
      
      setUsers(usersWithRoles);
      
    } catch (err) {
      console.error("‚ùå === ERRO CR√çTICO NA BUSCA ===", err);
      setError(err instanceof Error ? err : new Error('Erro desconhecido'));
    } finally {
      setLoading(false);
    }
  };
  
  useEffect(() => {
    console.log("üîÑ useUserData effect - isAuthenticated:", isAuthenticated);
    if (isAuthenticated) {
      fetchUsersFromDatabase();
    } else {
      console.log("‚ö†Ô∏è Usu√°rio n√£o autenticado, n√£o buscando usu√°rios");
      setLoading(false);
    }
  }, [isAuthenticated]);
  
  const addUser = (user: Omit<User, 'id' | 'createdAt'>) => {
    const newUser: User = {
      ...user,
      id: users.length > 0 ? Math.max(...users.map(u => u.id)) + 1 : 1,
      createdAt: new Date(),
    };
    setUsers([...users, newUser]);
  };
  
  const updateUser = (id: number, userData: Partial<User>) => {
    setUsers(users.map(user => 
      user.id === id ? { ...user, ...userData } : user
    ));
  };
  
  const deleteUser = (id: number) => {
    setUsers(users.filter(user => user.id !== id));
  };
  
  // Fun√ß√£o para recarregar os dados
  const refreshUsers = () => {
    console.log("üîÑ Recarregando usu√°rios...");
    if (isAuthenticated) {
      fetchUsersFromDatabase();
    }
  };
  
  return {
    users,
    filteredUsers,
    searchTerm,
    setSearchTerm,
    setUsers,
    loading,
    isLoading: loading,
    error,
    addUser,
    updateUser,
    deleteUser,
    refreshUsers
  };
};
